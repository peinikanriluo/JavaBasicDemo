package cn.I_basic.day02_TypeChange_Break;

public class TimeForOneDay {
    public static void main(String[] args) {
        for (int i = 0; i < 24; i++) {
            for (int i1 = 0; i1 < 60; i1++) {
                System.out.println(i + "hours"+"    " + i1 + "minutes");
            }
        }
    }
}
